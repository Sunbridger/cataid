-- ============================================================
-- 查询用户账号和明文密码
-- ============================================================
-- 注意：系统已配置为使用明文存储密码。
-- 请确保不要泄露此文件查询结果。
-- ============================================================

SELECT
    id AS "用户ID",
    phone AS "手机号",
    email AS "邮箱",
    nickname AS "昵称",
    role AS "角色",
    password_hash AS "密码(明文)"
FROM users
WHERE phone = '19999999999'  -- 替换为要查询的目标手机号
   OR email = 'test@example.com'
ORDER BY created_at DESC;

